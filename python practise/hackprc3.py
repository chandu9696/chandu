def Solve (N):
    # Write your code here
    if(N==1):
        return("NO")
    sum=1
    for i in range(2,N):
        if(N%i==0):
            sum=sum+i
    if(sum==N):
        return("YES")
    else:
        return("NO")



T = int(input())
for _ in range(T):
    N = int(input())
    out_ = Solve(N)
